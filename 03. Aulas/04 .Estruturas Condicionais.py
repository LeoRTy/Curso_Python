a = 5
b = 10

# if é "Se"

if a > b:
	print('a é maior que b')

if a < b:
	print('a é menor que b')


# else é "Senao"

if b < a:
	print('b é menor que a')
else:
	print('b não é menor que a')

# Elif para mais de uma condição

x = 1
