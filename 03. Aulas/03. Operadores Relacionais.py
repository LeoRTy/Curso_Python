# Operadores Relacionais

x = 2
y = 3

print(x == y)

''' 
== Operação que verifica se é igual
Se sim resultado é True
Caso não é False
'''

print(x > y) # X é Maior que Y ?
print(x < y) # X é Menor que Y ?
print(y > x) # Y é Maior que X ?

soma = x + y

print(soma) # Esta somando X e Y na variavel Soma

print(soma >= y) # Soma é Maior que Y ?